func TestVerifyHostnameInSNI(t *testing.T) {
	type args struct {
		b string
	}
	tests := []struct {
		name string
		args args
		want bool
	}{
		{
			name: "1",
			args: args{b: "invalid"},
			want: false,
		},
		{
			name: "2",
			args: args{b: "127.0.0.1"},
			want: false,
		},
		{
			name: "3",
			args: args{b: "example.co.m"},
			want: false,
		},
		{
			name: "4",
			args: args{b: "example..com"},
			want: false,
		},
		{
			name: "5",
			args: args{b: ".example.com"},
			want: false,
		},
		{
			name: "6",
			args: args{b: "example.com."},
			want: false,
		},
		{
			name: "7",
			args: args{b: "*.example.com"},
			want: false,
		},
		{
			name: "8",
			args: args{b: "i.i"},
			want: false,
		},
		{
			name: "9",
			args: args{b: "example.com"},
			want: true,
		},
		{
			name: "10",
			args: args{b: "exam-ple.com"},
			want: true,
		},
		{
			name: "11",
			args: args{b: "xn--example.com"},
			want: true,
		},
		{
			name: "12",
			args: args{b: "wWw.Example.coM"},
			want: true,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := VerifyHostnameInSNI(tt.args.b); got != tt.want {
				t.Errorf("VerifyHostnameInSNI() = %v, want %v", got, tt.want)
			}
		})
	}
}

